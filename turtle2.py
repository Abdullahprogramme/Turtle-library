import turtle
a=turtle.Turtle()
a.speed(1)

a.color("Blue","Cyan")
a.begin_fill()
a.forward(100)
a.left(90)
a.forward(100)
a.left(90)
a.forward(100)
a.left(90)
a.forward(100)
a.left(90)
a.end_fill()


a.penup()
a.forward(200)
a.pendown()

a.color("Green","Teal")
a.begin_fill()
a.forward(100)
a.left(90)
a.forward(100)
a.left(90)
a.forward(100)
a.left(90)
a.forward(100)
a.right(90)
a.end_fill()

a.color("Orange","Purple")
a.begin_fill()
a.forward(100)
a.right(90)
a.forward(100)
a.right(90)
a.forward(100)
a.right(90)
a.forward(100)
a.right(90)
a.end_fill()

turtle.done()